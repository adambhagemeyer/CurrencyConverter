/**
 * Class containing methods to convert Euros to 
 * other types of currency
 */

public class EuroConversion {
    
    public double EuroToDollar(double euro)
    {
        return euro * 1.13;
    }

    public double EuroToYen(double euro)
    {
        return euro * 129.38;
    }

    public double EuroToPound(double euro)
    {
        return euro * 0.85;
    }

    public double EuroToWon(double euro)
    {
        return euro * 1346.58;
    }

    public double EuroToPeso(double euro)
    {
        return euro * 23.48;
    }

    public double EuroToRupee(double euro)
    {
        return euro * 85.49;
    }
}
