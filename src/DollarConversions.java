/**
 * Java class containing methods to convert Dollars to other 
 * types of currency
 */

public class DollarConversions {
    
    public double DollarToEuro(double dollar)
    {
        return dollar * 0.88;
    }

    public double DollarToYen(double dollar)
    {
        return dollar * 114.20;
    }

    public double DollarToPound(double dollar)
    {
        return dollar * 0.75;
    }

    public double DollarToWon(double dollar)
    {
        return dollar * 1188.68;
    }

    public double DollarToPeso(double dollar)
    {
        return dollar * 20.73;
    }

    public double DollarToRupee(double dollar)
    {
        return dollar * 75.47;
    }
}
