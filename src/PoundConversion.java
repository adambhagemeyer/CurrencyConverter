/**
 * Class containing methods to convert pounds
 * to other types of currency
 */

public class PoundConversion {
    
    public double PoundToDollar(double pound)
    {
        return pound * 1.34;
    }

    public double PoundToEuro(double pound)
    {
        return pound * 1.18;
    }

    public double PoundToYen(double pound)
    {
        return pound * 152.44;
    }

    public double PoundToWon(double pound)
    {
        return pound * 1586.86;
    }

    public double PoundToPeso(double pound)
    {
        return pound * 27.66;
    }

    public double PoundToRupee(double pound)
    {
        return pound * 100.75;
    }
}
